
public class Book {
	private String title;
	private int price;
	public Book() {}//기본 생성자
	
	
	public String getTitle() {
		return title; 
		}
	public void setTitle(String title) {
		this.title = title;
	}//멤버변수 name 값 설정함수
	
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
}

